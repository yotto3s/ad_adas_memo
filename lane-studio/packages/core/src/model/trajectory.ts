/**
 * @file trajectory.ts
 * @description Vehicle trajectory model: keyframes and interpolated frames.
 *
 * The user places VehicleKeyframes on the map canvas. The interpolation
 * module generates dense frames between keyframes for export.
 */

import type { Pose2D } from "./geometry";

/** Unique identifier for a keyframe. */
export type KeyframeId = string & { readonly __brand: unique symbol };

/**
 * A user-placed keyframe defining the vehicle state at a point in time.
 *
 * Keyframes are the editable control points; the interpolation module
 * produces smooth trajectories between them.
 */
export interface VehicleKeyframe {
  readonly id: KeyframeId;

  /** Vehicle pose in map coordinates. */
  readonly pose: Pose2D;

  /** Vehicle speed in m/s. */
  readonly speed: number;

  /** Timestamp in seconds from the start of the scenario. */
  readonly timestamp: number;

  /**
   * User-defined attributes.
   * Schema-driven, for extensibility (e.g., acceleration, steering angle).
   */
  readonly attributes: Readonly<Record<string, string | number | boolean>>;
}

/**
 * An interpolated vehicle state at a specific timestamp.
 * Generated by the interpolation module; not directly user-editable.
 */
export interface VehicleFrame {
  /** Vehicle pose in map coordinates. */
  readonly pose: Pose2D;

  /** Vehicle speed in m/s. */
  readonly speed: number;

  /** Timestamp in seconds. */
  readonly timestamp: number;
}

/**
 * A complete trajectory: the ordered set of user-placed keyframes.
 * The interpolation module uses this to generate dense VehicleFrames.
 */
export interface Trajectory {
  /** Keyframes ordered by timestamp (ascending). */
  readonly keyframes: readonly VehicleKeyframe[];
}
